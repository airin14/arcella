namespace Arcella\Domain\Entities;

/**
 * Class User
 * @package Arcella\Domain\Entities
 */
class User
{
    private $name;

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
